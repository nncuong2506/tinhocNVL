<section id="lesson" class="card" data-view style="display:none">
  <h2>Nội dung bài: Quản lí dữ liệu trong máy tính</h2>

  <div class="lesson-section">
    <h3>1. Khái niệm về dữ liệu và tệp tin</h3>
    <p>
      Dữ liệu là thông tin được lưu trữ trong máy tính. Dữ liệu được lưu dưới dạng các <strong>tệp tin (file)</strong>.
      Mỗi tệp tin có tên và phần mở rộng (ví dụ: <code>vanban.docx</code>, <code>anh.jpg</code>, <code>baocao.xlsx</code>).
    </p>
  </div>

  <div class="lesson-section">
    <h3>2. Thư mục (Folder)</h3>
    <p>
      Thư mục là nơi dùng để chứa các tệp tin hoặc các thư mục con khác, giúp sắp xếp dữ liệu trong máy tính một cách khoa học.
    </p>
    <ul>
      <li>Mỗi thư mục có một tên riêng và có thể chứa nhiều tệp hoặc thư mục con.</li>
      <li>Cấu trúc lưu trữ trong máy tính được tổ chức theo dạng <strong>cây thư mục</strong> (directory tree).</li>
    </ul>
  </div>

  <div class="lesson-section">
    <h3>3. Các thao tác với tệp và thư mục</h3>
    <ul>
      <li><strong>Tạo</strong> tệp hoặc thư mục mới.</li>
      <li><strong>Đổi tên</strong> tệp hoặc thư mục.</li>
      <li><strong>Sao chép</strong> hoặc <strong>di chuyển</strong> đến vị trí khác.</li>
      <li><strong>Xóa</strong> tệp hoặc thư mục không cần thiết.</li>
      <li><strong>Xem</strong> hoặc <strongmở</strong> nội dung tệp tin.</li>
    </ul>
  </div>

  <div class="lesson-section">
    <h3>4. Đường dẫn và cây thư mục</h3>
    <p>
      Mỗi tệp tin hoặc thư mục trong máy tính đều có một <strong>đường dẫn (path)</strong> xác định vị trí của nó trong hệ thống.
    </p>
    <p>Ví dụ: <code>C:\Users\HocSinh\Documents\baocao.docx</code></p>
    <p>
      Trong đó:
      <ul>
        <li><code>C:</code> là ổ đĩa gốc.</li>
        <li><code>Users\HocSinh\Documents</code> là các thư mục con.</li>
        <li><code>baocao.docx</code> là tệp tin cuối cùng.</li>
      </ul>
    </p>
  </div>

  <div class="lesson-section">
    <h3>5. Ý nghĩa của việc quản lí dữ liệu</h3>
    <ul>
      <li>Giúp tìm kiếm, lưu trữ và sử dụng dữ liệu dễ dàng, nhanh chóng.</li>
      <li>Tránh thất lạc, nhầm lẫn hoặc mất mát thông tin.</li>
      <li>Tăng hiệu quả học tập, làm việc và sử dụng máy tính.</li>
    </ul>
  </div>

  <div class="lesson-section">
    <h3>6. Thực hành</h3>
    <ul>
      <li>Tạo thư mục mới mang tên của bạn trong ổ đĩa D.</li>
      <li>Trong thư mục đó, tạo 2 thư mục con: <code>Baitap</code> và <code>HinhAnh</code>.</li>
      <li>Tạo một tệp văn bản trong thư mục <code>Baitap</code> có tên <code>thuchanh.txt</code>.</li>
      <li>Thực hiện sao chép, di chuyển và xóa thử các tệp hoặc thư mục.</li>
    </ul>
  </div>
</section>
